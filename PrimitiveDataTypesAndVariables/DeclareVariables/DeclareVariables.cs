using System;

    class DeclareVariables
    {
        static void Main()
        {
            ushort number1 = 52129;
            sbyte number2 = -115;
            sbyte number3 = 97;
            short number4 = -10000;
            int number5 = 4825932;

            Console.WriteLine(
                "{0} is First Number {1} Second Number, {2} is Third Number, {3} is Fourth Number, {4} is Fifth Number", number1, number2, number3, number4, number5);
        }
    }
